"""Check for package.json without node_modules."""

from __future__ import annotations

from pathlib import Path

from checks.base import CheckResult, Status


def _install_command(project: Path) -> str:
    if (project / "pnpm-lock.yaml").is_file():
        return "pnpm install"
    if (project / "yarn.lock").is_file():
        return "yarn install"
    if (project / "bun.lockb").is_file():
        return "bun install"
    return "npm install"


def check_node_modules(project: Path) -> CheckResult:
    name = "Node Modules"
    package_json = project / "package.json"
    node_modules = project / "node_modules"

    if not package_json.is_file():
        return CheckResult(
            name=name,
            status=Status.PASS,
            message="No package.json found — Node dependencies not expected.",
        )

    if node_modules.is_dir() and any(node_modules.iterdir()):
        return CheckResult(
            name=name,
            status=Status.PASS,
            message="node_modules is present.",
        )

    return CheckResult(
        name=name,
        status=Status.FAIL,
        message="package.json exists but node_modules is missing.",
        fix_command=_install_command(project),
    )
